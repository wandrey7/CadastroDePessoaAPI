package com.wdev.sistema_de_cadastros.service;

public class Person {
    private String name;
    private String email;
    private int age;
    private String height;

    public Person(String name, String email, int age, String height) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.height = height;
    }
}
